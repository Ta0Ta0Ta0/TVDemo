package com.example.myapplication.media;

public class Song {

    private int code;
    private String msg;
    private ResDTOX res;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public ResDTOX getRes() {
        return res;
    }

    public void setRes(ResDTOX res) {
        this.res = res;
    }
}
